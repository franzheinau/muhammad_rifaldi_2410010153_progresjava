    public void loadDataKaryawan(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("nip");
        model.addColumn("nama Karyawan");
        model.addColumn("telp Karyawan");
        model.addColumn("golongan Karyawan");
        model.addColumn("status Nikah");
        model.addColumn("lama Kerja");
        for (int i = 0; i < obj.arrayNip().size(); i++) {
            Object[] barisData = {obj.arrayNip().get(i), obj.arrayNama().get(i), obj.arrayTelp().get(i), 
                obj.arrayGolongan().get(i), obj.arrayStatusNikah().get(i), obj.arrayLamaKerja().get(i) };
            model.addRow(barisData);
        }
        tableKaryawan.setModel(model);
    } 
